# Suma y resta (+ y -)
suma = 15 + 5  # Resultado: 20
resta = 15 - 5  # Resultado: 10

# Explicación:
# La suma realiza la operación 15 + 5, que da como resultado 20.
# La resta realiza la operación 15 - 5, que da como resultado 10.

# Multiplicación y división (* y /)
multi = 15 * 5  # Resultado: 75
division = 15 / 5  # Resultado: 3.0 (float)
division_entera = 15 // 5  # Resultado: 3 (entero)

# Explicación:
# La multiplicación realiza 15 * 5, que da como resultado 75.
# La división realiza 15 / 5, que da como resultado 3.0. El resultado es un float (número decimal).
# La división entera realiza 15 // 5, que da como resultado 3, descartando cualquier decimal.

# Potenciación (**)
exponente = 15 ** 2  # Resultado: 225

# Explicación:
# La potenciación realiza 15 elevado a la 2 (15^2), lo que da como resultado 225.

# Resto o módulo (%)
resto = 12 % 5  # Resultado: 2

# Explicación:
# El módulo calcula el resto de la división entre 12 y 5. En este caso, 12 dividido por 5 da un cociente de 2 y un resto de 2.

# Imprimir los resultados
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multi)
print("División:", division, "Tipo:", type(division))
print("División entera:", division_entera)
print("Potenciación:", exponente)
print("Módulo:", resto)
